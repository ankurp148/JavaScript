// abs()
console.log(Math.abs(-5)); // Output: 5

// cbrt()
console.log(Math.cbrt(27)); // Output: 3

// ceil()
console.log(Math.ceil(4.3)); // Output: 5

// floor()
console.log(Math.floor(4.9)); // Output: 4

// log()
console.log(Math.log(10)); // Output: 2.302585092994046

// max()
console.log(Math.max(5, 10, 3)); // Output: 10

// min()
console.log(Math.min(5, 10, 3)); // Output: 3

// pow()
console.log(Math.pow(2, 3)); // Output: 8

// random()
console.log(Math.random()); // Output: A random number between 0 and 1

// round()
console.log(Math.round(4.6)); // Output: 5

// sqrt()
console.log(Math.sqrt(16)); // Output: 4

// trunc() - No direct equivalent in JavaScript, but can be achieved using Math.trunc() or parseInt()
console.log(Math.trunc(4.9)); // Output: 4
console.log(parseInt(4.9)); // Output: 4
